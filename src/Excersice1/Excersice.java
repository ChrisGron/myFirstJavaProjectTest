package Excersice1;

public class Excersice {
    public static void main(String[] args) {

        int numberOne = 5;
        int numberTwo = 8;
        int numberThree = 14;

        System.out.println("Summan är " + (numberOne+numberTwo+numberThree));
        System.out.println("Medlvärdet är " + (numberOne+numberTwo+numberThree)/3);

        int rectangleSideA = 3;
        int rectangleSideB = 5;

        System.out.println("Arean är " + rectangleSideA*rectangleSideB);
        System.out.println("Omkretsen är " + 2*(rectangleSideA+rectangleSideB));
    }
}
